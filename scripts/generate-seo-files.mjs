import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");
const publicDir = path.join(projectRoot, "public");

const rawSiteUrl =
  process.env.SITE_URL ||
  process.env.VITE_SITE_URL ||
  "https://yash-kumar-portfolio.onrender.com";

const siteUrl = rawSiteUrl.replace(/\/+$/, "");
const today = new Date().toISOString().slice(0, 10);

const routes = [
  { path: "/", changefreq: "weekly", priority: "1.0" },
  { path: "/about", changefreq: "monthly", priority: "0.9" },
  { path: "/projects", changefreq: "weekly", priority: "0.9" },
  { path: "/skills", changefreq: "monthly", priority: "0.8" },
  { path: "/experience", changefreq: "monthly", priority: "0.8" },
  { path: "/education", changefreq: "yearly", priority: "0.7" },
  { path: "/certificates", changefreq: "monthly", priority: "0.7" },
  { path: "/contact", changefreq: "yearly", priority: "0.8" },
];

const sitemapEntries = routes
  .map(
    ({ path: routePath, changefreq, priority }) => `  <url>
    <loc>${siteUrl}${routePath}</loc>
    <lastmod>${today}</lastmod>
    <changefreq>${changefreq}</changefreq>
    <priority>${priority}</priority>
  </url>`,
  )
  .join("\n\n");

const sitemapXml = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"
        xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
        xsi:schemaLocation="http://www.sitemaps.org/schemas/sitemap/0.9
                            http://www.sitemaps.org/schemas/sitemap/0.9/sitemap.xsd">

${sitemapEntries}

</urlset>
`;

const robotsTxt = `# robots.txt generated by scripts/generate-seo-files.mjs
User-agent: *
Allow: /

Disallow: /api/
Disallow: /node_modules/

Allow: /sitemap.xml
Allow: /manifest.json
Allow: /og-image.png
Allow: /profile-photo.jpg
Allow: /favicon.ico
Allow: /*.pdf$

Sitemap: ${siteUrl}/sitemap.xml
`;

fs.writeFileSync(path.join(publicDir, "sitemap.xml"), sitemapXml, "utf8");
fs.writeFileSync(path.join(publicDir, "robots.txt"), robotsTxt, "utf8");

const indexPath = path.join(projectRoot, "index.html");
const indexHtml = fs.readFileSync(indexPath, "utf8");
const normalizedIndexHtml = indexHtml.replace(
  /https:\/\/yash-kumar-portfolio\.onrender\.com/g,
  siteUrl,
);
fs.writeFileSync(indexPath, normalizedIndexHtml, "utf8");

console.log(`SEO files generated for ${siteUrl}`);
